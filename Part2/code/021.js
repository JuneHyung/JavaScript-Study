// 산술등호연산
var x = 10; // x = 10
x += 5; // x = 15
x *= 2; // x = 30
console.log(x); // x = 30

var y = 10; // y = 10
y -= 5; // y = 5
y /= 5; // y = 1
console.log(6); // y = 1

// **********************************
// 나머지 연산자(%), 거듭제곱 연산자(**), 단향음수/양수(+/-), 증감 연산자(++ / --)
console.log(15 % 4); // 3
console.log(3 ** 3); // 27
console.log(+10); // 10
console.log(-10); // -10
var value = 10; // 10
++value; // 10 -> 11
--value; // 11 -> 10
console.log(value); // 10